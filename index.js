function add(a,b) {
    return a + b;
}
function subtract(a,b) {
    return a - b;
}
function multiply(a,b) {
    return a * b;
}
function divide(a,b) {
    return a / b;
}
function increment(n) {
    n++;
    return n;
}
function decrement(n) {
    n--;
    return n;
}
function makeInt(number="2") {
    return parseInt(number, 10);
}
function preserveDecimal(n="2.222") {
    return parseFloat(n);
}